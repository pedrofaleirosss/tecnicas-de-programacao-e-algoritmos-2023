    private void pegaSenha(String tipo){
        String sql = "Select min(tb01_num) from tb01_senha";
               sql += " where tb01_id_tipo = '" + tipo + "' and tb01_id_status = 'N'";
            
        try{


        conexao objetoBD = new conexao();
        
                        
        objetoBD.conectar();

        ResultSet rs = statement.executeQuery(sql);  
       
        if (rs.next()) 
        {
             minSenha = rs.getInt(1);
        }
       
        objetoBD.desconectar();
        }
        catch (Exception e){
             System.out.println(e.getMessage());
         }
    }
    
