    private void mostrar(){
        lbSenha.setText(tipo + minSenha);
        
        m.addElement("         " + lbSenha.getText());
        registros.setModel(m);
        
        Random sorte = new Random();
        sorteio = sorte.nextInt(9);
        sorteio = sorteio + 1;
        
        lbGuiche.setText("Guichê " + sorteio);
    }
    
   
    private void mouseMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_mouseMouseClicked
        
        tipo = "P";
        pegaSenha(tipo);
        lbSemSenhas.setText("");
        if(minSenha == 0){
            tipo = "N";
            pegaSenha(tipo);
            lbSemSenhas.setText("");
            if(minSenha == 0){
                lbSenha.setText("");
                lbSemSenhas.setText("Nenhuma senha!");
                lbGuiche.setText("");
               return;
            }
        }
        mostrar();
        gravaBanco();
        
    }//GEN-LAST:event_mouseMouseClicked

